function polybius(text) {
    const code = new Map([
        ['A', '11'], ['B', '12'], ['C', '13'], ['D', '14'], ['E', '15'],
        ['F', '21'], ['G', '22'], ['H', '23'], ['I', '24'], ['J', '24'],
        ['K', '25'], ['L', '31'], ['M', '32'], ['N', '33'], ['O', '34'],
        ['P', '35'], ['Q', '41'], ['R', '42'], ['S', '43'], ['T', '44'],
        ['U', '45'], ['V', '51'], ['W', '52'], ['X', '53'], ['Y', '54'],
        ['Z', '55'], ['a', '11'], ['b', '12'], ['c', '13'], ['d', '14'], 
        ['f', '21'], ['g', '22'], ['h', '23'], ['i', '24'], ['j', '24'],
        ['k', '25'], ['l', '31'], ['m', '32'], ['n', '33'], ['o', '34'],
        ['p', '35'], ['q', '41'], ['r', '42'], ['s', '43'], ['t', '44'],
        ['u', '45'], ['v', '51'], ['w', '52'], ['x', '53'], ['y', '54'],
        ['z', '55'], ['e', '15'], [' ', ' ']
    ]);
    const decode = new Map([
        ['11', 'a'], ['12', 'b'], ['13', 'c'], ['14', 'd'], ['15', 'e'],
        ['21', 'f'], ['22', 'g'], ['23', 'h'], ['24', 'i'], ['55', 'z'],
        ['25', 'k'], ['31', 'l'], ['32', 'm'], ['33', 'n'], ['34', 'o'],
        ['35', 'p'], ['41', 'q'], ['42', 'r'], ['43', 's'], ['44', 't'],
        ['45', 'u'], ['51', 'v'], ['52', 'w'], ['53', 'x'], ['54', 'y'],
    ]);
    let ans = ''
    if(('a' <= text[0] && text[0] <= 'z') || ('A' <= text[0] && text[0] <= 'Z')) {
        for(let x of text) {
            if(('a' > x || x > 'z') && ('A' > x || x > 'Z') && x != ' ') continue 
            ans += code.get(x)
        }
        return ans
    } 
    let cur = '', pre = 'a'
    for(let x of text) {
        if(x == ' ') {
            if(pre != x) ans += ' '
        }
        else cur += x
        if(cur.length == 2) {
            ans += decode.get(cur)
            cur = ''
        } 
        pre = x
    }
    return ans
}
